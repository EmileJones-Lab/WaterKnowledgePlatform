package top.emilejones.hhu.splitter

import org.junit.jupiter.api.Test
import top.emilejones.hhu.spliter.PunctuationSplitter

class PunctuationSplitterTest {


    @Test
    fun case1(){
        val maxSequenceLength = 200
        val text = """
            春天的校园里，花开遍地，柳条轻摇，微风拂面，让人心情格外舒畅；同学们三三两两地走在小路上，讨论着即将到来的运动会，空气中弥漫着青草与花朵的清香。操场上正在进行一场激烈的足球赛，场边的观众兴奋地呐喊助威，仿佛要将整个操场的气氛推向高潮。
            然而，在教学楼的某间教室里，一位年轻的老师正给学生们讲述着一个长长的故事，这个故事描绘了一个小镇从贫瘠到富饶的过程，其中经历了无数次的洪水与干旱，而居民们依然顽强地生活着，他们用双手修筑堤坝、开垦荒地、种植粮食，甚至在资源极度匮乏的时期也从不放弃希望，因为他们相信只要团结一致、努力奋斗，就一定能够改变家园的面貌，这种精神感动了每一个聆听的人。窗外阳光透过玻璃洒在黑板上，仿佛为这个故事镀上了一层温暖的光辉。
            晚风渐起，天边染上了淡淡的橘红色，操场上的孩子们依然在奔跑、嬉笑、追逐，那一幕幕如同定格在画布上的瞬间，令人难以忘怀。
        """.trimIndent()
        val respectResult = """
            春天的校园里，花开遍地，柳条轻摇，微风拂面，让人心情格外舒畅；同学们三三两两地走在小路上，讨论着即将到来的运动会，空气中弥漫着青草与花朵的清香。操场上正在进行一场激烈的足球赛，场边的观众兴奋地呐喊助威，仿佛要将整个操场的气氛推向高潮。
            然而，在教学楼的某间教室里，一位年轻的老师正给学生们讲述着一个长长的故事，这个故事描绘了一个小镇从贫瘠到富饶的过程，其中经历了无数次的洪水与干旱，而居民们依然顽强地生活着，他们用双手修筑堤坝、开垦荒地、种植粮食，甚至在资源极度匮乏的时期也从不放弃希望，因为他们相信只要团结一致、努力奋斗，就一定能够改变家园的面貌，这种精神感动了每一个聆听的人。
            窗外阳光透过玻璃洒在黑板上，仿佛为这个故事镀上了一层温暖的光辉。晚风渐起，天边染上了淡淡的橘红色，操场上的孩子们依然在奔跑、嬉笑、追逐，那一幕幕如同定格在画布上的瞬间，令人难以忘怀。
        """.trimIndent()
        val result = PunctuationSplitter.split(text, maxSequenceLength).getOrThrow().joinToString(separator = "\n")
        assert(result == respectResult)
    }

